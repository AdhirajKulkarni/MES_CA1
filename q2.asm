ORG 0000H

MOV R0, 50H     ; A
MOV R1, 51H     ; B

LOOP:
MOV A, R0
JZ A_ZERO

MOV A, R1
JZ B_ZERO

DEC R0
DEC R1
SJMP LOOP

A_ZERO:
MOV A, R1
JZ EQUAL
MOV 52H, #0FFH  ; A < B
SJMP ENDPRG

B_ZERO:
MOV 52H, #01H   ; A > B
SJMP ENDPRG

EQUAL:
MOV 52H, #00H   ; A = B

ENDPRG:
HERE: SJMP HERE
END
